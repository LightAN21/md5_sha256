#include "ft_printf.h"

int	re_atoi(char *s, int *i)
{
	int	j;
	int	ans;

	ans = 0;
	j = 0;
	while ('0' <= s[j] && s[j] <= '9' && s[j] != '\0')
		ans = 10 * ans + s[j++] - '0';
	*i += j;
	return (ans);
}
