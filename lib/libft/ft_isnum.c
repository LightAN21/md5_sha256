#include "libft.h"

int	ft_isnum(const char *s)
{
	int		i;
	char	*n;

	if (!s)
		return (0);
	i = -1;
	n = (char *)s;
	if (n[0] == '-')
		i++;
	while (n[++i])
	{
		if (n[i] < '0' || n[i] > '9')
			return (0);
	}
	return (1);
}
