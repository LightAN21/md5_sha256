#include "ft_ssl.h"

uint32_t	md5_f0(uint32_t t[])
{
	return ((t[1] & t[2]) | (~t[1] & t[3]));
}

uint32_t	md5_f1(uint32_t t[])
{
	return ((t[3] & t[1]) | (~t[3] & t[2]));
}

uint32_t	md5_f2(uint32_t t[])
{
	return (t[1] ^ t[2] ^ t[3]);
}

uint32_t	md5_f3(uint32_t t[])
{
	return (t[2] ^ (t[1] | ~t[3]));
}
